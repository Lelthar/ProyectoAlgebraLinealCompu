     int Orden=0;
     JTextPane[][] Entrada;
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    
        for(int i=0;i<Orden;i++){
            for(int k=0;k<Orden;k++){  
                pnMatriz.remove(Entrada[i][k]);
            }
        }
        
        Orden=Integer.parseInt(txtOrden.getText().toString());
        
        if(Orden>5){
            JOptionPane.showMessageDialog(null, "No se puede ingresar una matriz mas grande que orden 5", "Aviso:", JOptionPane.INFORMATION_MESSAGE);
            Orden=5;
        }else if(Orden<1){
            JOptionPane.showMessageDialog(null, "No se puede ingresar una matriz mas pequeÃ±a que orden 1", "Aviso:", JOptionPane.INFORMATION_MESSAGE);
            Orden=1;
        }
        GridBagConstraints gbc = new GridBagConstraints();
        pnMatriz.setLayout(new GridBagLayout());
        Entrada=new JTextPane[Orden][Orden];      
        
        Border line = BorderFactory.createLineBorder(Color.DARK_GRAY);
        Border empty = new EmptyBorder(0, 0, 0, 0);
        CompoundBorder border = new CompoundBorder(line, empty);
        
        int contX=0,contY=0;
        for(int i=0;i<Orden;i++){
            contX=0;
            for(int k=0;k<Orden;k++){  
                Entrada[i][k]=new JTextPane();
                Entrada[i][k].setBorder(border);
                Entrada[i][k].setPreferredSize(new Dimension(25,23));
                gbc.gridx = contX;
                gbc.gridy = contY;

                pnMatriz.add(Entrada[i][k],gbc);        
                
                contX++;
            }   
            contY++;
        }
        
        pnMatriz.revalidate();
        pnMatriz.repaint();
    }//GEN-LAST:event_jButton1ActionPerformed

